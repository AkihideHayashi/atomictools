from math import pi

m = 1.0
s = 1.0
kg = 1.0
A = 1.0
mol = 1.0
cd = 1.0
K = 1.0

C = s * A
N = m * kg / (s * s)
Pa = N / (m * m)
J = N * m
W = J / s


h  = 6.62607015E-34 * J * s                    # Planck constant
e  = 1.602176634E-19 * C                       # elementary charge
kb = 1.380649E-23 * J / K                      # Boltzmann constant
NA = 6.02214076E23 / mol                      # Avogadro constant
c  = 299792458 * m / s                         # speed of light

hbar = h / (2 * pi)
me = 9.10938356E-31 * kg
a0 = 0.52917721067E-10 * m # Bohr radius
Eh = 4.359744650E-18 * J   # hartree

Da = 1.660539040E-27 * kg
cal = 4.184 * J
eV = 1.6021766208E-19 * J